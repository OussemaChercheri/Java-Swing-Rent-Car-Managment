    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // Récupération des dates dans les zones de texte
        String debutStr = dtdebut.getText();
        String finStr = dtfin.getText();

        // Conversion des dates en objets Date
        Date debut = null;
        Date fin = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            debut = sdf.parse(debutStr);
            fin = sdf.parse(finStr);
        } catch (Exception ex) {
            nbj.setText("Format de date invalide");
            return;
        }

        // Calcul du nombre de jours
        long diffMillis = fin.getTime() - debut.getTime();
        int nbJours = (int) (diffMillis / (1000 * 60 * 60 * 24) + 1);

        // Affichage du nombre de jours dans la zone de texte nbj
        nbj.setText(Integer.toString(nbJours));

    }//GEN-LAST:event_jButton8ActionPerformed

