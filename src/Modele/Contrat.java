    private void genererPdf(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genererPdf
        try {
            // Create a new iText Document object
            Document document = new Document(PageSize.A4, 50, 50, 50, 50);
            PdfWriter.getInstance(document, new FileOutputStream("contrat.pdf"));
            document.open();
            
            // Add a Paragraph to the document with the current date and time
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            Date date = new Date();
            Paragraph dateTime = new Paragraph("Generated on " + dateFormat.format(date),
                    FontFactory.getFont(FontFactory.HELVETICA, 14, Font.NORMAL));
            dateTime.setAlignment(Element.ALIGN_CENTER);
            document.add(dateTime);
            document.add(new Paragraph("\n\n"));
            
            // Add the Swing screen contents to the document
            // Example: Add a table with some data
            
            document.add(new Paragraph("----------------------------------------"));
            document.add(new Paragraph("----------------------------------------"));
            document.add(new Paragraph("Matricule de Contrat :"+ txtmatriculec.getText()));
            document.add(new Paragraph("Matricule de voiture :"+txtmatricule.getSelectedItem()));
            document.add(new Paragraph("ID client :"+txtid.getSelectedItem()));
            document.add(new Paragraph("Date debut :"+dtdebut.getText()));
            document.add(new Paragraph("Date fin :"+dtfin.getText()));
            int nbj1=Integer.parseInt(nbj.getText());
            document.add(new Paragraph("Nombre de jour :"+nbj1));
            int pr=Integer.parseInt(txtprixvoiture.getText());
            document.add(new Paragraph("Prix de jour :"+pr));
            document.add(new Paragraph("Prix totale :"+txtprix.getText()));
            document.add(new Paragraph("----------------------------------------"));
            document.add(new Paragraph("----------------------------------------"));

            // Save and close the document
            document.close();
            JOptionPane.showMessageDialog(null, "PDF generated successfully!");
             
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error generating PDF: " + ex.getMessage());
        }
    }//GEN-LAST:event_genererPdf

