    private boolean verify(String matricule, String dd, String df, String nb, String pj, String pt) {
    // Vérification du matricule
    boolean is_valid = true;
    matValid.setText("");
    ddValid.setText("");
    dfValid.setText("");
    nbValid.setText("");
    pjValid.setText("");
    ptValid.setText("");
    
    if(nb==null || nb.isEmpty()){
            nbValid.setText("cliquer sur set jour");
            is_valid=false;
    }
    if(pj==null || pj.isEmpty()){
            pjValid.setText("cliquer sur set voiture");
            is_valid=false;
    }
    if(pt==null || pt.isEmpty()){
            nbValid.setText("cliquer sur set prix");
            is_valid=false;
    }
    if (matricule == null || !matricule.matches("[A-Za-z0-9-]+")) {
        matValid.setText("matricule invalide");
        is_valid=false;
    }
    
    LocalDate locDate;
    try {
        locDate = LocalDate.parse(dd, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        if (locDate.isBefore(LocalDate.now())) {
            ddValid.setText("la date doit etre au futur");
            is_valid= false;
        }
    } catch (DateTimeParseException e) {
        ddValid.setText("date format dd/MM/yyyy");
        is_valid= false;
    }
    LocalDate locDate2;
    try {
        locDate2 = LocalDate.parse(df, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        locDate = LocalDate.parse(dd, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
        if (locDate2.isBefore(locDate)) {
            dfValid.setText("la date doit etre > date debut");
            is_valid= false;
        }
    } catch (DateTimeParseException e) {
        dfValid.setText("date format dd/MM/yyyy");
        is_valid= false;
    }
    
    return is_valid;
}
